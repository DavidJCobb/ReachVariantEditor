   namespace {
      class _index_cache {
         protected:
            static bool is_lower(QChar c) { return c >= 0x61 && c <= 0x7A; }
            static bool is_upper(QChar c) { return c >= 0x41 && c <= 0x5A; }

            _index_cache() {
               auto& list = all_entities;
               auto  size = list.size();
               QChar last = '\0';
               for (decltype(size) i = 0; i < size; ++i) {
                  auto& def = list[i];
                  QChar f   = def.name[0];
                  if (f == last)
                     continue;
                  if (is_lower(f)) {
                     last = f;
                     this->lower[f.unicode() - 0x61] = i;
                  }
                  if (is_upper(f)) {
                     last = f;
                     this->upper[f.unicode() - 0x41] = i;
                  }
               }
            }
         public:
            std::array<size_t, 26> lower = {};
            std::array<size_t, 26> upper = {};
            bool generated = false;

            static _index_cache& get() {
               static _index_cache instance;
               return instance;
            }

            size_t start_from(QChar c) const noexcept {
               if (is_upper(c))
                  return this->upper[c.unicode() - 0x41];
               if (is_lower(c))
                  return this->lower[c.unicode() - 0x61];
               return 0;
            }
      };

      using index_t = std::pair<QChar, size_t>;
      static std::array<index_t, 26> _indices = {{}};
   }
   extern QString look_up_entity(const QString& name) {
      if (name.isEmpty())
         return QString();
      size_t i    = _index_cache::get().start_from(name[0]);
      size_t size = all_entities.size();
      for (; i < size; ++i) {
         auto& e = all_entities[i];
         if (e.name == name)
            return e.replace;
      }
      return QString();
   }
